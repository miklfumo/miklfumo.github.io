function get_gallery(): array {
    $years = ['2024', '2025'];
    $gallery = [];

    foreach ($years as $year) {
        $dir = dirname(__DIR__) . '/public/images/' . $year;
        $images = [];

        if (is_dir($dir)) {
            $files = glob($dir . '/*.{jpg,jpeg,png,webp,JPG,JPEG,PNG,WEBP}', GLOB_BRACE) ?: [];
            sort($files, SORT_NATURAL | SORT_FLAG_CASE);

            foreach ($files as $file) {
                $images[] = [
                    'src' => 'public/images/' . $year . '/' . basename($file),
                    'alt' => 'Фотографии конференции ' . $year,
                ];
            }
        }

        $gallery[] = [
            'year' => $year,
            'images' => $images,
        ];
    }

    return $gallery;
}
